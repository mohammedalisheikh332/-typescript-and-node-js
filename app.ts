let mark: number = 55;
if (mark >= 90){
    console.log("Grade 90 A+");
}else if (mark >= 80){
    console.log("Grade 80 A");
} else if (mark >= 70){
    console.log("Grade 70 B");
} else if (mark >= 60){
    console.log("Grade 60 C");
}else if (mark >= 50){
    console.log("Grade 50 D");
}else{
    console.log("Grade:F");
}

    
